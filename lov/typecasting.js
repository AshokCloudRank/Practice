// String to Number
let str1 = "123";
let num1 = Number(str1);
console.log("Original Value:", str1); 
console.log("Changed Value:", num1);

// Number to String
let num2 = 456;
let str2 = String(num2);
console.log("Original Value:", num2,); 
console.log("Changed Value:", str2);

